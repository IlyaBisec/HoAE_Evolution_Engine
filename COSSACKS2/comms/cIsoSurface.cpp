// cIsoSurface::Init
//-----------------------------------------------------------------------------------------------------------------------
void cIsoSurface::Init(const cVec3 &bbMin, const cVec3 &bbMax, const int NCellsX, const int NCellsY, const int NCellsZ) {
	m_NCellsX = NCellsX, m_NCellsY = NCellsY, m_NCellsZ = NCellsZ;
	
	// Direction vectors for normal computation:
	m_Dx = cVec3((bbMax.x - bbMin.x) / float(8.0f * NCellsX), 0.0f, 0.0f);
	m_Dy = cVec3(0.0f, (bbMax.y - bbMin.y) / float(8.0f * NCellsY), 0.0f);
	m_Dz = cVec3(0.0f, 0.0f, (bbMax.z - bbMin.z) / float(8.0f * NCellsZ));
	
	// Nodes:
	m_Nodes.SetCount((NCellsX + 1) * (NCellsY + 1) * (NCellsZ + 1));
	Node *pNode = m_Nodes.ToPtr();
	cVec3 Pos;
	int iz, iy, ix;
	for(iz = 0; iz <= NCellsZ; iz++) {
		Pos.z = cMath::Lerp(bbMin.z, bbMax.z, float(iz) / NCellsZ);
		for(iy = 0; iy <= NCellsY; iy++) {
			Pos.y = cMath::Lerp(bbMin.y, bbMax.y, float(iy) / NCellsY);
			for(ix = 0; ix <= NCellsX; ix++) {
				Pos.x = cMath::Lerp(bbMin.x, bbMax.x, float(ix) / NCellsX);
				pNode->p = Pos;
				pNode++;
			}
		}
	}

	// Edges:
	int NEdges = 3 * NCellsX * NCellsY * NCellsZ +
		2 * (NCellsX * NCellsY + NCellsX * NCellsZ + NCellsY * NCellsZ) + NCellsX + NCellsY + NCellsZ;
	m_Edges.SetCount(NEdges);
	Edge *pEdge = m_Edges.ToPtr();

	// Cells:
	m_Cells.SetCount(NCellsX * NCellsY * NCellsZ);
	Cell *pCell = m_Cells.ToPtr();

#define NewEdge(e, n0, n1) \
	pEdge->pNode0 = pCell->pNodes[n0]; \
	pEdge->pNode1 = pCell->pNodes[n1]; \
	pCell->pEdges[e] = pEdge; \
	pEdge++;

#define OldEdge(e, ix, iy, iz, n) \
	pCell->pEdges[e] = m_Cells[((iz) * NCellsY + (iy)) * NCellsX + (ix)].pEdges[n];

	for(iz = 0; iz < NCellsZ; iz++) {
		for(iy = 0; iy < NCellsY; iy++) {
			for(ix = 0; ix < NCellsX; ix++) {
				pCell->pNodes[0] = &m_Nodes[(iz * (NCellsY + 1) + iy) * (NCellsX + 1) + ix];
				pCell->pNodes[1] = &m_Nodes[(iz * (NCellsY + 1) + iy) * (NCellsX + 1) + ix + 1];
				pCell->pNodes[2] = &m_Nodes[(iz * (NCellsY + 1) + iy + 1) * (NCellsX + 1) + ix];
				pCell->pNodes[3] = &m_Nodes[(iz * (NCellsY + 1) + iy + 1) * (NCellsX + 1) + ix + 1];
				pCell->pNodes[4] = &m_Nodes[((iz + 1) * (NCellsY + 1) + iy) * (NCellsX + 1) + ix];
				pCell->pNodes[5] = &m_Nodes[((iz + 1) * (NCellsY + 1) + iy) * (NCellsX + 1) + ix + 1];
				pCell->pNodes[6] = &m_Nodes[((iz + 1) * (NCellsY + 1) + iy + 1) * (NCellsX + 1) + ix];
				pCell->pNodes[7] = &m_Nodes[((iz + 1) * (NCellsY + 1) + iy + 1) * (NCellsX + 1) + ix + 1];

				if(0 == iz) {
					if(0 == iy) {
						NewEdge(0, 0, 1);
					} else {
						OldEdge(0, ix, iy - 1, iz, 2);
					}
					NewEdge(1, 1, 3);
					NewEdge(2, 2, 3);
					if(0 == ix) {
						NewEdge(3, 0, 2);
					} else {
						OldEdge(3, ix - 1, iy, iz, 1);
					}
				} else {
					OldEdge(0, ix, iy, iz - 1, 4);
					OldEdge(1, ix, iy, iz - 1, 5);
					OldEdge(2, ix, iy, iz - 1, 6);
					OldEdge(3, ix, iy, iz - 1, 7);
				}

				if(0 == iy) {
					NewEdge(4, 4, 5);
				} else {
					OldEdge(4, ix, iy - 1, iz, 6);
				}
				NewEdge(5, 5, 7);
				NewEdge(6, 6, 7);
				if(0 == ix) {
					NewEdge(7, 4, 6);
				} else {
					OldEdge(7, ix - 1, iy, iz, 5);
				}


				if(0 == iy) {
					if(0 == ix) {
						NewEdge(8, 0, 4);
					} else {
						OldEdge(8, ix - 1, iy, iz, 9);
					}
					if(0 == iy) {
						NewEdge(9, 1, 5);
					}
				} else {
					OldEdge(8, ix, iy - 1, iz, 11);
					OldEdge(9, ix, iy - 1, iz, 10);
				}
				NewEdge(10, 3, 7);
				if(0 == ix) {
					NewEdge(11, 2, 6);
				} else {
					OldEdge(11, ix - 1, iy, iz, 10);
				}
				pCell++;
			}
		}
	}
}

