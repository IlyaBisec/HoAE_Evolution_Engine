//-----------------------------------------------------------------------------
// cIsoSurface::Update
//-----------------------------------------------------------------------------
void cIsoSurface::Update(FieldFunc pFn) {
	int i;
	for(i = 0; i < m_Nodes.Count(); i++) {
		Node &n = m_Nodes[i];
		n.f = pFn(n.p);
	}

	m_Verts.Clear();
	m_Indices.Clear();

	Vertex u;
	float f;
	for(i = 0; i < m_Edges.Count(); i++) {
		Edge &e = m_Edges[i];
		if(cMath::Sign(e.pNode0->f) * cMath::Sign(e.pNode1->f) == -1) {
			u.Pos = e.pNode0->p + (e.pNode0->f / (e.pNode0->f - e.pNode1->f)) * (e.pNode1->p - e.pNode0->p);
			f = pFn(u.Pos);
			u.Normal = cVec3::Normalize(cVec3(f - pFn(u.Pos + m_Dx), f - pFn(u.Pos + m_Dy), f - pFn(u.Pos + m_Dz)));
			e.i = m_Verts.Add(u);
		}
	}

	for(i = 0; i < m_Cells.Count(); i++) {
		Cell &c = m_Cells[i];
		unsigned int CubeIndex = 0;
		for(int j = 0; j < 8; j++) {
			if(c.pNodes[j]->f < 0) {
				CubeIndex |= 1 << j;
			}
		}

		int NInds = comms::MCTris[CubeIndex][0];
		if(NInds) {
			unsigned int *pInds = comms::MCTris[CubeIndex] + 1;
			for(int j = 0; j < NInds; j++) {
				m_Indices.Add(c.pEdges[pInds[j]]->i);
			}
		}
	}
}